let day_name = "sat";

switch (day_name) {
  case "mon":
    console.log(`First Day`);
    break;
  case "tue":
    console.log(`2 Day`);
    break;
  case "wed":
    console.log(`Third Day`);
    break;
  case "thur":
    console.log(`4 Day`);
    break;
  case "fri":
    console.log(`5 Day`);
    break;
  case "sat":
    console.log(`6 Day`);
    break;
  case "sun":
    console.log(`7 Day`);
    break;

  default:
    console.log(`Default Is Terminated`);

    break;
}
